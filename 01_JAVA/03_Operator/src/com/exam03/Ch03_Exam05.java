package com.exam03;

public class Ch03_Exam05 {

	 public static void main(String[] args) {
		int su = 4;
		
		System.out.println("4*2 = " + (su << 1));
		System.out.println("4*4 = " + (su << 2));
		System.out.println("4*8 = " + (su << 3));

		System.out.println("4/2 = " + (su >> 1));
		System.out.println("4/4 = " + (su >> 2));
		System.out.println("4/8 = " + (su >> 3));
		
		
	}
}
